# 리스트 연산자
# +	        결합 연산자
# *	        반복 연산자
# []	    선택 연산자
# [:]	    범위 선택 연산자
# == 	    일치 연산자
# del	    삭제 연산자
# in        in 연산자
# not in    not in 연산자


# 리스트 선언
list1 = [1, 2, 3]
list2 = [4, 5, 6]

# 리스트 + 연산자
list3 = list1 + list2
print("리스트 + 연산자", list3)

# 리스트 * 연산자
list4 = list1 * 7
print("리스트 * 연산자", list4)

# 선택 연산자
list_e = [1, 2, 3]
del list_e[2:4]
print("del list_e[2:4] >> ", list_e)


